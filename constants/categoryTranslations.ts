export const getLegacyCategoryLabel = (key?: string): string | undefined => {
    if (!key) return undefined;

    if (CATEGORY_TRANSLATIONS[key]) {
        return CATEGORY_TRANSLATIONS[key];
    }

    const keyLower = key.toLowerCase();
    const matchedKey = Object.keys(CATEGORY_TRANSLATIONS)
        .find((k) => k.toLowerCase() === keyLower);

    return matchedKey ? CATEGORY_TRANSLATIONS[matchedKey] : undefined;
};
